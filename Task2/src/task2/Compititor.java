
package task2;


public class Compititor {
    private String compititorNumber;
    private String name;

    public Compititor(String compititorNumber, String name) {
        this.compititorNumber = compititorNumber;
        this.name = name;
    }

    public String getCompititorNumber() {
        return compititorNumber;
    }

    public String getName() {
        return name;
    }

    public void setCompititorNumber(String compititorNumber) {
        this.compititorNumber = compititorNumber;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
    
}

